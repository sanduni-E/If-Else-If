
package ifelseif;


public class IfElseIF {

    
    public static void main(String[] args) {
       int num=5;
       if(num>6){
           System.out.println("number is greater than 5");
       }
       else if(num<4){
           System.out.println("number is less than 5");
       }
       else{
           System.out.println("number is equal to 5");
       }
    }
    
}
